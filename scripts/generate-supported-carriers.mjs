// Scans public/carriers/ and writes the list of carrier slugs that have an
// actual logo file on disk. Run automatically before `next dev` / `next build`
// (see package.json's predev/prebuild) so the app's carrier catalog always
// reflects exactly what's present in public/carriers/ — no manual pruning.
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const carriersDir = path.join(root, "public", "carriers");
const outPath = path.join(root, "src", "lib", "data", "supported-carrier-slugs.generated.ts");

const LOGO_EXTENSIONS = /\.(svg|png|jpe?g|webp)$/i;

const files = fs.existsSync(carriersDir) ? fs.readdirSync(carriersDir) : [];
const slugs = files
  .filter((f) => LOGO_EXTENSIONS.test(f) && f.toLowerCase() !== "default.svg")
  .map((f) => f.replace(LOGO_EXTENSIONS, ""))
  .sort();

const content = `// AUTO-GENERATED by scripts/generate-supported-carriers.mjs — do not edit by hand.
// Regenerated on every \`npm run dev\` / \`npm run build\` from whatever logo
// files are present in public/carriers/ (excluding default.svg, the fallback).
export const SUPPORTED_CARRIER_SLUGS: readonly string[] = ${JSON.stringify(slugs)};
`;

fs.mkdirSync(path.dirname(outPath), { recursive: true });
fs.writeFileSync(outPath, content);

console.log(
  `[generate-supported-carriers] Found ${slugs.length} carrier logo(s) in public/carriers/${
    slugs.length ? ": " + slugs.join(", ") : " (none yet — only default.svg is present)"
  }`
);
